        【卷十】【犬部】狗
        孔子曰：“狗，叩也。叩气吠以守。”从犬，句声。古厚切 [12]
        说文解字注
        （狗）孔子曰：“狗、叩也。叩气吠以守。”
        注：吠以当作以吠。许书有扣无叩。扣训牵马也。疑古本有叩字、而许逸之。叩、触也。从卩、口声。叩气者、出其气也。一说叩即敂之俗。敂者、击也。几以此击彼皆曰敂。犬敂气吠亦是以内御外。《鸿范五行传》注曰：“犬、畜之以口吠守者也。属言。”按《释兽》云：未成豪、狗。与马二岁曰驹、熊虎之子曰豿同义。皆谓稚也。
        从犬。句声。
        注：古厚切。四部。 [13]
        康熙字典
        《唐韵》古后切。《集韵》《韵会》《正韵》举后切，并音苟。《说文》：孔子曰：狗，叩也。叩气吠以守。从犬，句声。《尔雅·释畜》：未成毫，狗。注：狗子未生㲦毛者。又：尨，狗也。《易·说卦》：艮为狗。《礼·曲礼》：尊客之前不叱狗。
