        又星名。《史记·天官书》：天狗，状如大奔星。
        又鸟名。《尔雅·释鸟》：鴗，天狗。注：小鸟也。靑似翠，食鱼，江东呼为水狗。
        又《扬子·方言》：蝼蛄，南楚谓之杜狗。
        又木狗。《正字通》：熊大古冀越集云：木狗，生广东左右江山中，形如黑狗，能登木。皮为衣𥛑，能运动脚气。元世祖足疾，取其皮为袴。
        又溪狗，虫名。《正字通》：陈藏器曰：生南方溪㵎𤰞处，状如虾蟇，尾长三四寸，治溪毒游蛊。
        又草名。《尔雅·释草》：蘻，狗毒。疏：蘻，一名狗毒。
        又地名。《齐语》：以燕为主，反其㑴地，柴夫吠狗。注：燕之二邑。
